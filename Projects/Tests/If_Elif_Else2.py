numero = 10
if numero < 0:
    print("El número es negativo")
elif numero == 0:
    print("El número es cero")
elif numero > 0 and numero <= 10:
    print("El número es positivo y menor o igual a 10")
elif numero > 10 and numero <= 20:
    print("El número es positivo y está entre 11 y 20")
else:
    print("El número es positivo y mayor a 20")


